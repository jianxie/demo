package domain;

public class User {
	private String fname;
	private Integer fage;
	
	public User(){}

	public String getFname() {
		return fname;
	}

	public void setFname(String fname) {
		this.fname = fname;
	}

	public Integer getFage() {
		return fage;
	}

	public void setFage(Integer fage) {
		this.fage = fage;
	}
	
	
}
